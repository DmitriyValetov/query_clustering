category_classification_prompt_single = dedent("""
Ты — экспертный классификатор поисковых запросов. Твоя задача — проанализировать запрос пользователя и определить категорию на основе того, ЧТО ПОЛЬЗОВАТЕЛЬ ХОЧЕТ ПОЛУЧИТЬ ОТ СИСТЕМЫ.

Категория должна описывать НАМЕРЕНИЕ и ДЕЙСТВИЕ пользователя, а не тематику запроса.

Примеры правильных категорий (для ориентира, но ты можешь создавать свои):
- "Найти информацию" - пользователь хочет найти и получить информацию о чем-то
- "Подготовить презентацию" - пользователь хочет создать презентацию
- "Объяснить ошибку в коде" - пользователь хочет понять и исправить ошибку в программе
- "Сравнить сущности" - пользователь хочет сравнить несколько объектов по параметрам
- "Создать документ" - пользователь хочет создать текстовый документ
- "Проанализировать данные" - пользователь хочет проанализировать данные и получить выводы
- "Найти товары/услуги" - пользователь хочет найти и выбрать товары или услуги
- "Решить задачу" - пользователь хочет решить конкретную задачу или проблему
- "Сгенерировать контент" - пользователь хочет создать новый контент (текст, изображение и т.д.)
- "Перевести текст" - пользователь хочет перевести текст
- "Объяснить понятие" - пользователь хочет получить объяснение какого-то понятия

Важно:
- Категория должна быть краткой (2-4 слова) и описывать ДЕЙСТВИЕ/НАМЕРЕНИЕ
- Категория должна быть на русском языке
- Фокусируйся на том, ЧТО пользователь хочет получить, а не на ТЕМЕ запроса
- Ты можешь использовать предложенные категории ИЛИ создать свою, если она лучше описывает намерение пользователя
- Если ни одна категория (включая свои) не подходит точно, используй "else"

Запрос: {query}

Верни только название категории, без дополнительных пояснений, без кавычек и точек в конце.
""").strip()

